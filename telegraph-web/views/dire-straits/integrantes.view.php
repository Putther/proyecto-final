    <h1>LOS COMIENZOS (1977-1979)</h1><br>
    <p>La banda comenzó teniendo a los clásicos cuatro integrantes que componían una banda de rock en aquellos años como por ejemplo los Beatles: Un cantante y/o guitarrista principal, un bajo, una guitarra rítmica y una batería. Como se podrá ir observando, poco a poco fue adquiriendo más y más integrantes con más variedad de instrumentos que le daban su toque a la banda y que suponían todo un factor evolutivo.</p><br>
