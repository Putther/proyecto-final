    <h1 class="titulo-card-2">LA ETAPA FINAL (1990-1995)</h1><br>
    <p>Nos encontramos ya en la última etapa de la banda y también la más numerosa contando con un total de nueve músicos en los escenarios. Varios instrumentos nuevos se unen también al plantel siendo la guitarra de pedal de acero y la percusión. La banda ya tenía más que definido su sonido y con estas nuevas adiciones adquirían un toque más country.</p><br>

