    <h1 class="titulo-card-1">LA ERA DORADA (1980-1989)</h1><br>
    <p>A partir de la década de los 80s y con una fama cada vez más y más en auge, Dire Straits ampliaba su número de integrantes con nuevos instrumentos como el teclado o el saxofón y acabarían poco a poco adquiriendo un sonido más complejo y progresivo. Ya los únicos integrantes originales que quedaban eran Mark y John, quienes estarían juntos hasta el final.</p><br>

